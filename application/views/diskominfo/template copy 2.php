        <script>
    function updateClock() {
        var currentDate = new Date();

        // Menyimpan nama hari dalam bahasa Indonesia
        var days = ['Minggu', 'Senin', 'Selasa', 'Rabu', 'Kamis', 'Jumat', 'Sabtu'];
        var currentDay = days[currentDate.getDay()];

        var hours = currentDate.getHours();
        var minutes = currentDate.getMinutes();
        var seconds = currentDate.getSeconds();

        // Menambahkan angka 0 di depan jika nilai jam, menit, atau detik kurang dari 10
        hours = (hours < 10) ? '0' + hours : hours;
        minutes = (minutes < 10) ? '0' + minutes : minutes;
        seconds = (seconds < 10) ? '0' + seconds : seconds;

        var jam = document.getElementById('dynamic-day');
        jam.innerHTML = currentDay + ', ' + currentDate.getDate() + ' ' + getMonthName(currentDate.getMonth()) + ' ' + currentDate.getFullYear() + ' / ' + hours + ':' + minutes + ':' + seconds + ' WIB';
    }

    function getMonthName(month) {
        var months = ['Januari', 'Februari', 'Maret', 'April', 'Mei', 'Juni', 'Juli', 'Agustus', 'September', 'Oktober', 'November', 'Desember'];
        return months[month];
    }

    // Memanggil fungsi updateClock setiap detik
    setInterval(updateClock, 1000);

    // Memanggil fungsi updateClock saat halaman pertama kali dimuat
    updateClock();
</script>

