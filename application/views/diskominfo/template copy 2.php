<script>
    let checkboxes = [false, false, false, false];
    let votes = JSON.parse(localStorage.getItem('votes')) || [0, 0, 0, 0];

    function toggleCheckbox(index) {
        checkboxes[index - 1] = !checkboxes[index - 1];
    }

    function vote() {
        for (let i = 0; i < checkboxes.length; i++) {
            if (checkboxes[i]) {
                votes[i]++;
                checkboxes[i] = false;
            }
        }
        updateResults();
    }

    function hoverOption(element) {
        element.style.backgroundColor = '#2980b9';
    }

    function unhoverOption(element) {
        element.style.backgroundColor = 'transparent';
    }

    function submitPolling() {
        vote();
        alert("Terima kasih atas partisipasinya!");
        // Anda bisa menambahkan logika untuk mengirim hasil polling ke server di sini
    }

    function updateResults() {
        const totalVotes = votes.reduce((acc, curr) => acc + curr, 0);

        for (let i = 0; i < votes.length; i++) {
            const percentage = totalVotes === 0 ? 0 : (votes[i] / totalVotes) * 100;
            const resultBar = document.getElementById(`result-bar-${i + 1}`);
            resultBar.style.width = `${percentage}%`;
            resultBar.textContent = `${percentage.toFixed(2)}%`;
        }

        // Simpan hasil polling ke localStorage
        localStorage.setItem('votes', JSON.stringify(votes));
    }

    // Memastikan hasil polling ditampilkan saat halaman pertama kali dimuat
    updateResults();
</script>





